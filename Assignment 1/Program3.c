#include<stdio.h>

////////////////////////////////////////////////////////////////
// 
//  Function Name : Display()
//  Input         : Nothing
//  Output        : Display the numbers on screen                      
//            
////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt=0;
    iCnt=5;
    while (iCnt>=1)
    {
        printf("%d" , iCnt);
        iCnt++;
    }
}
////////////////////////////////////////////////////////////////
// Entry point function
////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

