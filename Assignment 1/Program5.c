#include<stdio.h>

////////////////////////////////////////////////////////////////
// 
//  Function Name : Accept()
//  Input         : Accept one number
//  Output        : Display * times on screen                       
//            
////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt=0;
    for(iCnt=1; iCnt<=iNo; iCnt++)
    {
        printf("*");
    }
}
////////////////////////////////////////////////////////////////
// Entry point function
////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    iValue=5;

    Accept(iValue);

    return 0;
}
